def compute_sum_of(num_1,num_2):
    my_sum = num_1  + num_2
    return my_sum

def compute_difference_of(num_1,num_2):
    my_diff = num_1 - num_2
    return my_diff

def compute_product_of(num_1,num_2):
    my_product = num_1 * num_2
    return my_product

def compute_division_of(num_1,num_2):
    my_division = num_1 / num_2
    return my_division
